def fizzbuzz(argument)
  if argument / 3 == TRUE
    return "Fizz"
    
  else if argument / 5 == TRUE
    return "Buzz"
    
  if argument / 3 == TRUE && argument / 5 == TRUE
    return "FizzBuzz"

  else
  return nil
  
end
